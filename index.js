
//comparison operators

let x = 1;

// relational

console.log(x > 0);
console.log(x >= 0);
console.log(x < 1);
console.log(x <= 1);

// strict equality (type + value)

console.log(1 === 1);

console.log(x === 1);
console.log(x !== 1);

// loose equality (if types don't match, only checks values)

console.log('1' == 1); // converts right to string
console.log(true == 1); // converts right to boolean

